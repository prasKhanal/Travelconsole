   private void loadFileActionPerformed(ActionEvent evt) {//GEN-FIRST:event_loadFileActionPerformed
       // TODO add your handling code here:
       loadFileChooser.setVisible(true);
       if (loadFileChooser.showOpenDialog(this) == JFileChooser.APPROVE_OPTION){
           try{
               FilePath=loadFileChooser.getSelectedFile().getPath();
               //get input from another
               FileInputStream stream=new FileInputStream(FilePath);
               ObjectInputStream input = new ObjectInputStream(stream);
               agent=(TravelAgent)input.readObject();
               //asign static value lastCustomerId for Customer class
               agent.runMaxCustomerId();
               //asign static value of lastFlightId to flight class
               agent.runMaxFlightId();
               
           }
           catch (Exception e) {
               //if there is no .txt file
               System.out.println( loadFileChooser.getSelectedFile().getPath()+"\nDo you want To Enter New Agent.\nEnter 1 For \"Yes\"  \n0 For \"No\""  );
               
           }
       }
   }//GEN-LAST:event_loadFileActionPerformed

